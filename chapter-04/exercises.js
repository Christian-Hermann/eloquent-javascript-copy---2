// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

/*
I: takes two numbers start and end
O: ann array of all the number between start and end 
C: NA
E: NA
*/

function range(start, end, step = 1) {
 
 
  let output = [];
 // return an emtpy array if range is the same
if (start === end){
  return []
}



if (step > 0){
  for (let i = start; i <= end; i += step){
    output.push(i)
  }
} else if (step < 0){
  for (let i = start; i >= end; i += step){
    output.push(i)
  }
}




return output

}

