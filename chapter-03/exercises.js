/*
I: takes in a string and a character string that indicates charcter to be counted
O: returns a number of how many character strings are in the string
C:
E:
*/


function countChars(string, char) {
  // initialize counter to start at zero
 var count = 0;
 // iterate through the string
for(var i = 0; i < string.length; i++){
  // chekc to see if element is equal to given character
  if(string[i] === char){
    // if found increment each time
    count ++
  }
}
return count
}

