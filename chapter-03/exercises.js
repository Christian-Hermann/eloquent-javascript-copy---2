/*
I: takes in two numbers
O: returns the smallest argument
C: NA 
E: NA

*/
function min(num1, num2){
  // check to see if num1 is less than num2
if (num1 < num2){
  // if less return num1
  return num1
  // otherwise return num2
} else {
  return num2
}
}

